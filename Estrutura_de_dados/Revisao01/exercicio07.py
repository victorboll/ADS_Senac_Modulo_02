"""Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário."""

lado = float(input('Digite o lado do quadrado em centímetros: '))
area = lado**2
print(f'A área de um quadrado de lado {lado} centímetros quadrados, é {area} centímetros. O dobro dessa área é {2*area} centímetros quadrados.')
