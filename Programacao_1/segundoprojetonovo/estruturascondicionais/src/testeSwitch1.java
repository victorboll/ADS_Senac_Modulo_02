
public class testeSwitch1 {

	public static void main(String[] args) {
		int numero = 3;
		switch (numero) {
		case 1:
			System.out.println("variavel numero igual a 1");
			break;
		case 2:
			System.out.println("variavel numero igual a 2");
			break;
		default:
			System.out.println("variavel numero diferente de 2 e 1");
			break;
		}

	}

}
