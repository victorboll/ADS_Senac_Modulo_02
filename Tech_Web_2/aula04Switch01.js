/*Se você tem muitas condições, você pode usar o switchem vez de usar if e else if diversas vezes*/

var a = 5;
switch(a){
    case 0:
        console.log("Zero");
        break;
    case 1:
        console.log("Um");
        break;
    case 2:
        console.log("Dois");
        break;
    case 3:
        console.log("Três");
        break;
    default:
        console.log("Outro gato");    
}